def Normal2Torch(normal, resolution):
    W, H = resolution
    if len(normal.shape) == 4:
        normal = torch.from_numpy(normal).permute(0, 3, 1, 2) # -> b ch h w
    elif len(normal.shape)==3:
        normal = torch.from_numpy(normal)[None] # ch h w -> 1 ch h w
    else:
        raise RuntimeError('unexpected normal format ')

    normal = F.resize(normal, (H, W))
    return normal.squeeze()
